package Basics;

public class intByInt {
    public static void main(String[] args) {
        System.out.println(5/2);//intByInt = int
        System.out.println(5.0/2.0);//doubleByDouble = double
        System.out.println(5.0/2);//doubleByInt = double
        System.out.println(5/2.0);//intByDouble = double

    }
}
